const setText = (selector, value, root = document) => {
  const element = root.querySelector(selector);
  if (element) {
    element.textContent = value;
  }
};

const setTexts = (items, values, root = document) => {
  items.forEach((selector, index) => {
    setText(selector, values[index], root);
  });
};

const setSectionDescription = (selector, value, root = document) => {
  setText(`${selector} p:not(.eyebrow)`, value, root);
};

const applyCards = (containerSelector, cards) => {
  const cardElements = document.querySelectorAll(`${containerSelector} .equipment-card`);

  cards.forEach((card, index) => {
    const element = cardElements[index];
    if (!element) return;

    const image = element.querySelector("img");
    const tag = element.querySelector(".fleet-tag");
    const title = element.querySelector("h3");
    const text = element.querySelector("p");

    if (image && card.alt) image.alt = card.alt;
    if (tag && card.tag) tag.textContent = card.tag;
    if (title && card.title) title.textContent = card.title;
    if (text && card.text) text.textContent = card.text;
  });
};

const applyCopy = (lang) => {
  const copy = translations[lang] || translations.en;

  document.documentElement.lang = lang;
  document.title = copy.title;

  const description = document.querySelector('meta[name="description"]');
  if (description) {
    description.content = copy.description;
  }

  setTexts(
    [
      selectors.nav.services,
      selectors.nav.equipment,
      selectors.nav.lenses,
      selectors.nav.production,
      selectors.nav.contact,
    ],
    [
      copy.nav.services,
      copy.nav.equipment,
      copy.nav.lenses,
      copy.nav.production,
      copy.nav.contact,
    ]
  );

  setText(selectors.nav.cta, copy.nav.cta);
  selectors.nav.aria && document.querySelector(selectors.nav.aria)?.setAttribute("aria-label", copy.nav.aria);

  setText(".hero .eyebrow", copy.hero.eyebrow);
  setText(".hero h1", copy.hero.title);
  setText(".hero .lead", copy.hero.lead);
  setText(".hero .button-primary", copy.hero.primary);
  setText(".hero .button-secondary", copy.hero.secondary);

  const statItems = document.querySelectorAll(".hero .stats li");
  statItems.forEach((item, index) => {
    const pair = copy.hero.stats[index];
    if (!pair) return;
    const [title, text] = pair;
    const strong = item.querySelector("strong");
    const span = item.querySelector("span");
    if (strong) strong.textContent = title;
    if (span) span.textContent = text;
  });

  setText(".hero .card-label", copy.hero.brandLabel);
  setText(".hero .card-content h2", copy.hero.brandTitle);
  setText(".hero .card-note", copy.hero.brandNote);

  const miniCards = document.querySelectorAll(".hero .mini-card");
  miniCards.forEach((item, index) => {
    const pair = copy.hero.mini[index];
    if (!pair) return;
    const [label, value] = pair;
    const span = item.querySelector("span");
    const strong = item.querySelector("strong");
    if (span) span.textContent = label;
    if (strong) strong.textContent = value;
  });

  const splitCards = document.querySelectorAll(".section-split .split-card");
  const splitContent = [copy.split.rentals, copy.split.services];
  splitCards.forEach((item, index) => {
    const content = splitContent[index];
    if (!content) return;
    const eyebrow = item.querySelector(".eyebrow");
    const title = item.querySelector("h2");
    const text = item.querySelector("p");
    if (eyebrow) eyebrow.textContent = content.eyebrow;
    if (title) title.textContent = content.title;
    if (text) text.textContent = content.text;
  });

  setText('#services .section-heading .eyebrow', copy.services.eyebrow);
  setText('#services .section-heading h2', copy.services.title);
  setSectionDescription('#services .section-heading', copy.services.desc);
  const serviceCards = document.querySelectorAll("#services .feature-card");
  serviceCards.forEach((item, index) => {
    const card = copy.services.cards[index];
    if (!card) return;
    const title = item.querySelector("h3");
    const text = item.querySelector("p");
    if (title) title.textContent = card.title;
    if (text) text.textContent = card.text;
  });

  setText('#equipment .section-heading .eyebrow', copy.equipment.eyebrow);
  setText('#equipment .section-heading h2', copy.equipment.title);
  setSectionDescription('#equipment .section-heading', copy.equipment.desc);
  applyCards("#equipment", copy.equipment.cards);

  const equipmentStrip = document.querySelectorAll("#equipment .inventory-strip > div");
  equipmentStrip.forEach((item, index) => {
    const pair = copy.equipment.strip[index];
    if (!pair) return;
    const [title, text] = pair;
    const strong = item.querySelector("strong");
    const span = item.querySelector("span");
    if (strong) strong.textContent = title;
    if (span) span.textContent = text;
  });

  setText('#lenses .section-heading .eyebrow', copy.lenses.eyebrow);
  setText('#lenses .section-heading h2', copy.lenses.title);
  setSectionDescription('#lenses .section-heading', copy.lenses.desc);
  applyCards("#lenses", copy.lenses.cards);

  const lensStrip = document.querySelectorAll("#lenses .inventory-strip > div");
  lensStrip.forEach((item, index) => {
    const pair = copy.lenses.strip[index];
    if (!pair) return;
    const [title, text] = pair;
    const strong = item.querySelector("strong");
    const span = item.querySelector("span");
    if (strong) strong.textContent = title;
    if (span) span.textContent = text;
  });

  setText("#production .eyebrow", copy.production.eyebrow);
  setText("#production h2", copy.production.title);
  const productionParagraphs = document.querySelectorAll("#production .lead");
  if (productionParagraphs[0]) productionParagraphs[0].textContent = copy.production.lead1;
  if (productionParagraphs[1]) productionParagraphs[1].textContent = copy.production.lead2;
  setText("#production .process-panel h3", copy.production.workflowTitle);
  const workflowSteps = document.querySelectorAll("#production .steps li");
  workflowSteps.forEach((item, index) => {
    const pair = copy.production.steps[index];
    if (!pair) return;
    const [title, text] = pair;
    const strong = item.querySelector("strong");
    const span = item.querySelector("span");
    if (strong) strong.textContent = title;
    if (span) span.textContent = text;
  });

  setText("#vision .vision-card .eyebrow", copy.vision.eyebrow);
  const visionCards = document.querySelectorAll("#vision .vision-card");
  if (visionCards[0]) {
    setText("h2", copy.vision.title, visionCards[0]);
    setText("p:not(.eyebrow)", copy.vision.text, visionCards[0]);
  }
  if (visionCards[1]) {
    setText(".eyebrow", copy.vision.accentEyebrow, visionCards[1]);
    setText("h2", copy.vision.accentTitle, visionCards[1]);
    setText("p:not(.eyebrow)", copy.vision.accentText, visionCards[1]);
  }

  setText("#contact .eyebrow", copy.contact.eyebrow);
  setText("#contact h2", copy.contact.title);
  setText("#contact .lead", copy.contact.lead);
  setText("#contact .contact-card h3", copy.contact.quick);
  const contactLinks = document.querySelectorAll("#contact .contact-card a");
  if (contactLinks[0]) contactLinks[0].textContent = copy.contact.whatsapp;
  if (contactLinks[1]) contactLinks[1].textContent = copy.contact.email;
  if (contactLinks[2]) contactLinks[2].textContent = copy.contact.quote;

  setText(".site-footer p:last-child", copy.footer);

  document.querySelectorAll(".lang-button").forEach((button) => {
    const active = button.dataset.lang === lang;
    button.classList.toggle("is-active", active);
    button.setAttribute("aria-pressed", active ? "true" : "false");
  });

  localStorage.setItem("ardi-language", lang);
};

const getInitialLanguage = () => {
  const saved = localStorage.getItem("ardi-language");
  if (saved === "en" || saved === "es") return saved;

  const browserLanguage = (navigator.language || "").toLowerCase();
  if (browserLanguage.startsWith("es")) return "es";

  return "en";
};

const revealTargets = document.querySelectorAll(
  [
    ".hero-copy",
    ".hero-card",
    ".feature-card",
    ".equipment-card",
    ".inventory-strip",
    ".process-panel",
    ".vision-card",
    ".contact-card",
    ".section",
  ].join(", ")
);

revealTargets.forEach((element) => {
  element.classList.add("reveal");
});

const observer = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add("is-visible");
        observer.unobserve(entry.target);
      }
    });
  },
  { threshold: 0.16 }
);

revealTargets.forEach((element) => observer.observe(element));

const initialLanguage = getInitialLanguage();
applyCopy(initialLanguage);

document.querySelectorAll(".lang-button").forEach((button) => {
  button.addEventListener("click", () => {
    applyCopy(button.dataset.lang || "en");
  });
});
